package com.td.Entity;

public class Entity {
	private double x;
	private double y;
	private int texture_number;
	
	public void setX(double x) {
		this.x = x;
	}
	
	public double getX() {
		return this.x;
	}
	
	public void setY(double y) {
		this.y = y;
	}

	public double getY() {
		return this.y;
	}
	
	public int getTextureNumber() {
		return this.texture_number;
	}
	
	public void setTextureNumber(int num) {
		this.texture_number = num;
	}
	
}
