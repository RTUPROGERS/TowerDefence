package com.td.Entity;

public class Entity {
	private int x;
	private int y;
	private int texture_number;
	
	public void setX(int x) {
		this.x = x;
	}
	
	public int getX() {
		return this.x;
	}
	
	public void setY(int y) {
		this.y = y;
	}

	public int getY() {
		return this.y;
	}
	
	public int getTextureNumber() {
		return this.texture_number;
	}
	
}
